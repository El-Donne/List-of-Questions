# Dia da semana de acordo com valor

numero_escolhido = int(input("Escolha um dia da semana: \
    1 - Domingo \
    2 - Segunda\
    3 - Terça\
    4 - Quarta \
    5 - Quinta \
    6 - Sexta \
    7 - Sábado\n"))

if (numero_escolhido == 1):
    print("Domingo")
elif (numero_escolhido == 2):
    print("Segunda")
elif (numero_escolhido == 3):
    print("Terça")
elif (numero_escolhido == 4):
    print("Quarta")
elif (numero_escolhido == 5):
    print("Quinta")
elif (numero_escolhido == 6):
    print("Sexta")
elif (numero_escolhido == 7):
    print("Sábado")
else:
    print("[ERRO]")